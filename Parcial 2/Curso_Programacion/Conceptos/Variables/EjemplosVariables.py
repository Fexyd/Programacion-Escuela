'''
Vamos a ver ejemplos prácticos de variables en Python.
Python es un lenguaje de programacion muy sencillo y 
por lo mismo lo usaremos. 
'''

# Ejemplo 1: Variables son como cajas donde guardamos cosas
nombre = "Juan"
edad = 20
altura = 1.75
le_gusta_programar = True

# "print" es el equivalente a "cout" de c++
print(nombre) #Imprime: Juan
print(edad) #Imprime 20
print(altura) #Imprime 1.75
print(le_gusta_programar) #Imprime: True

'''
Lo ideal seria que puedan ejecutar este codigo en algun
Lugar para dejarlo lo mas claro posible
'''

# Ejemplo 2: Podemos cambiar lo que guardan las variables
print(nombre) #Imprime: Juan
nombre = "María"  # Ahora guardamos otro nombre
print(nombre) #Imprime: Maria

# Ejemplo 3: Podemos hacer operaciones con las variables
dinero = 100
gastos = 30
dinero_restante = dinero - gastos 
'''
Dinero restante es otra variable que almacena el 
Resultado de dinero - gastos
'''

print(dinero) #Imprime: 100
print(gastos) #Imprime: 30
print(dinero_restante) #Imprime: 70

