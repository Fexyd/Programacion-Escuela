'''
=== Sintaxis ===

La sintaxis es lo que se podria decir
que nos indica como escribir correctamente
el codigo. Es fundamental escribir
correctamente la sintaxis al codificar,
de lo contrario el codigo no funcionara.

Un ejemplo de sintaxis en c++:

#include <iostream>

en este ejemplo, el signo "#include" 
representaun comando o orden, en este 
caso significa que se va a añadir una 
libreria, no es necesario entender 
que es una libreria pero si es
necesario entender que es estrictamente 
necesario escribir correctamente
la sintaxis.
'''



